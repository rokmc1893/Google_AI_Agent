"""샘플 계약서 생성 스크립트 — 독소조항이 포함된 위험한 계약서"""
from docx import Document
from pathlib import Path

doc = Document()

doc.add_heading("소프트웨어 개발 용역 계약서", 0)
doc.add_paragraph(
    "본 계약은 (주)테크솔루션(이하 '갑')과 김철수(이하 '을') 사이에 "
    "소프트웨어 개발 용역에 관하여 다음과 같이 체결한다."
)

doc.add_heading("제1조 (목적)", level=1)
doc.add_paragraph(
    "제1항 본 계약은 갑이 을에게 위탁한 소프트웨어 개발 용역의 수행에 관한 "
    "기본적인 사항을 정함을 목적으로 한다."
)

doc.add_heading("제2조 (계약 기간)", level=1)
doc.add_paragraph(
    "제1항 계약 기간은 2024년 1월 1일부터 2024년 12월 31일까지로 한다.\n"
    "제2항 계약 종료 30일 전까지 어느 일방의 서면 해지 통보가 없으면 "
    "동일 조건으로 자동 갱신된다.\n"
    "제3항 자동 갱신 횟수에는 제한이 없으며, 을은 이에 이의를 제기할 수 없다."
)

doc.add_heading("제3조 (용역 대금 및 지급)", level=1)
doc.add_paragraph(
    "제1항 용역 대금은 금 5,000만원(부가가치세 포함)으로 한다.\n"
    "제2항 갑은 계약 체결 후 30일 이내에 착수금 1,000만원을 지급한다.\n"
    "제3항 잔금 4,000만원은 최종 납품 후 60일 이내에 지급한다.\n"
    "제4항 을이 대금 지급을 요청하는 경우, 갑의 재량으로 지급 시기를 조정할 수 있다."
)

doc.add_heading("제4조 (손해배상 및 위약금)", level=1)
doc.add_paragraph(
    "제1항 을이 본 계약을 위반하거나 납기를 준수하지 못하는 경우, "
    "을은 갑에 대하여 계약 총액의 30%에 해당하는 위약금을 지급하여야 한다.\n"
    "제2항 을의 귀책 사유로 계약이 해지되는 경우, "
    "을은 기 지급받은 모든 대금을 전액 반환하여야 한다.\n"
    "제3항 갑의 귀책 사유로 계약이 해지되는 경우에도, "
    "을은 별도의 손해배상을 청구할 수 없으며 갑은 면책된다."
)

doc.add_heading("제5조 (지식재산권)", level=1)
doc.add_paragraph(
    "제1항 본 계약의 이행과정에서 을이 개발한 소프트웨어, 소스코드, "
    "문서 및 이와 관련된 모든 권리는 갑에게 귀속된다.\n"
    "제2항 을이 계약 이전에 보유한 기술, 아이디어, 방법론을 활용하여 "
    "개발한 결과물에 대한 저작권 및 특허를 포함한 모든 지식재산도 갑에게 양도한다."
)

doc.add_heading("제6조 (비밀유지)", level=1)
doc.add_paragraph(
    "제1항 을은 본 계약 수행 중 지득한 갑의 영업비밀, 기술정보, 고객정보 및 "
    "기타 일체의 정보를 계약 종료 후 10년간 제3자에게 누설하여서는 아니 된다.\n"
    "제2항 을은 계약 종료 후 5년간 동종업계 취업 및 유사 사업을 영위할 수 없다.\n"
    "제3항 비밀유지 의무 위반 시 을은 계약 총액의 50%를 위약금으로 지급한다."
)

doc.add_heading("제7조 (계약 변경 및 해지)", level=1)
doc.add_paragraph(
    "제1항 갑은 필요하다고 판단하는 경우 일방적으로 계약 내용을 변경할 수 있으며, "
    "을은 이에 즉시 따라야 한다.\n"
    "제2항 갑은 언제든지 사전 통보 없이 계약을 해지할 수 있으며, "
    "이 경우 갑은 기 지급된 대금의 반환을 청구할 수 있다."
)

doc.add_heading("제8조 (분쟁 해결)", level=1)
doc.add_paragraph(
    "제1항 본 계약에서 발생하는 분쟁은 갑의 주소지 관할 법원을 전속 관할 법원으로 한다.\n"
    "제2항 소송 제기 시 패소한 당사자는 상대방의 변호사 비용 일체를 부담한다."
)

doc.add_paragraph(
    "\n\n위 계약의 내용을 확인하고 이에 동의하여 본 계약서 2부를 작성하여 각 1부씩 보관한다.\n\n"
    "2024년 1월 1일\n\n"
    "갑: (주)테크솔루션  대표이사 홍길동  (인)\n"
    "을: 김철수  주민등록번호: 800101-1234567  (인)"
)

out_path = Path("sample_contract_dangerous.docx")
doc.save(str(out_path))
print(f"샘플 계약서 생성 완료: {out_path.absolute()}")
